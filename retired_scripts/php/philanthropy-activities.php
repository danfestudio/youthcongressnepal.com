				$('.post-timeline').find('.entry:not(.entry-date-section)').each( function(){
					let position = $(this).position();
					if( position.left === 0 ) {
						$(this).removeClass('alt');
					} else {
						$(this).addClass('alt');
					}
					$(this).find('.entry-timeline').fadeIn();
				});

